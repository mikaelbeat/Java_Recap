package strings;

public class String_comparison {
	
	
	public static void main(String[] args) {
		
		String name = "Hello";
		String name2 = "hello";
		
		System.out.println(name.equals(name2));
		System.out.println(name == name2);
		System.out.println(name.equalsIgnoreCase(name2));
		
	}

}
